/******************************************************************************

Напишете програма, която създава матрица, чиито стойности над главния диагонал са равни на 1;

*******************************************************************************/

#include <iostream>

const int MAX_ROWS = 100;
const int MAX_COLS = 100;

using namespace std;

int main() {
    int matrix[MAX_ROWS][MAX_COLS];
    int rows, cols;

    // Въведете размерите на матрицата (брой редове и колони)
    cout << "Въведете брой редове: ";
    cin >> rows;
    cout << "Въведете брой колони: ";
    cin >> cols;

    // Инициализация и запълване на матрицата
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            if (i > j) {
                matrix[i][j] = 0;  // Стойности под главния диагонал
            } else {
                matrix[i][j] = 1;  // Стойности над главния диагонал
            }
        }
    }

    // Извеждане на матрицата
    cout << "Резултатната матрица:" << endl;
    for (int i = 0; i < rows; i++) {
        for (int j = 0; j < cols; j++) {
            cout << matrix[i][j] << " ";
        }
        cout << endl;
    }

    return 0;
}
